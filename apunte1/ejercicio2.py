#!/usr/bin/env python3

# Desarrollar un software que le solicite al usuario sus datos personales, 
# a saber: DNI, nombre y apellido, dirección y teléfono. Mostrar los datos 
# por pantalla.

dni = input("Ingrese DNI: ")
name = input("Ingrese nombre: ")
surname = input("Ingrese apellido: ")
address = input("Ingrese dirección: ")
phone = input("Ingrese teléfono: ")

print("DNI: " + dni)
print("Nombre: " + name)
print("Apellido: " + surname)
print("Dirección: " + address)
print("Teléfono: " + phone)
